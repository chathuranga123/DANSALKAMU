package com.dansala.bean.dansala;

public class DansalaBean {
	
	private int id;
	private String name;
	private float longitute;
	private float latitute;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the longitute
	 */
	public float getLongitute() {
		return longitute;
	}
	/**
	 * @param longitute the longitute to set
	 */
	public void setLongitute(float longitute) {
		this.longitute = longitute;
	}
	/**
	 * @return the latitute
	 */
	public float getLatitute() {
		return latitute;
	}
	/**
	 * @param latitute the latitute to set
	 */
	public void setLatitute(float latitute) {
		this.latitute = latitute;
	}



}
