package com.dansala.bean.dansalcategory;

public class DansalCategory {
	
	private int categotyId;
	private String type;
	private int iconId;
	
	/**
	 * @return the categotyId
	 */
	public int getCategotyId() {
		return categotyId;
	}
	/**
	 * @param categotyId the categotyId to set
	 */
	public void setCategotyId(int categotyId) {
		this.categotyId = categotyId;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the iconId
	 */
	public int getIconId() {
		return iconId;
	}
	/**
	 * @param iconId the iconId to set
	 */
	public void setIconId(int iconId) {
		this.iconId = iconId;
	}

}
